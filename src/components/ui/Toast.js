const styles = {
  success: 'border-green-500/30 bg-green-900/20',
  error: 'border-red-500/30 bg-red-900/20',
  warning: 'border-yellow-500/30 bg-yellow-900/20',
  info: 'border-blue-500/30 bg-blue-900/20',
};

function Toast({ toast, onClose }) {
  const [isExiting, setIsExiting] = useState(false);

  const handleClose = () => {
    setIsExiting(true);
    setTimeout(onClose, 300);
  };

  return (
    <div
      className={`
        flex items-center gap-3 px-4 py-3 rounded-xl border shadow-xl
        min-w-[300px] max-w-[400px]
        ${styles[toast.type]}
        ${isExiting ? 'animate-slideOutRight' : 'animate-slideInRight'}
      `}
    >
      {icons[toast.type]}
      <p className="text-white text-sm flex-1">{toast.message}</p>
      <button
        onClick={handleClose}
        className="text-gray-400 hover:text-white transition-colors"
      >
        <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
        </svg>
      </button>
    </div>
  );
}

